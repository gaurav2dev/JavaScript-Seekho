const grade = "A";

switch (grade) {
  case "A":
    console.log("Great");
    break;
  case "B":
    console.log("Excellent");
    break;
  case "C":
    console.log("Very Good");
    break;
  case "D":
    console.log("Can do better");
    break;
  case "E":
    console.log("Jaakr Padhai Kar!!!!!!");
    break;

  default:
    console.log("Error In The Grade");
    break;
}
